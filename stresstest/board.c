void shuffle(int m, int pieces){
    char * str = sudokuPuzzles[m].board + 2;
    int jmp = 2 * sudokuPuzzles[m].size + 1; 
    int sign1,sign2;
    int count=0;


    for(int k=0; k< 2 && count < pieces ; k++){
        for(int l=0; l< 2 && count < pieces; l++){

            sign1 = 1 - 2*k; // and : where true is negative  false is positive
            sign2 = 1 - 2 * ( (1 - k)*l + k*(1 - l) );// xor : where true is negative  false is positive


            for(int i=0; i< 4 + l && count < pieces; i++){

                if( !(l && i == 2) ){
                    *str = '0';
                    count++;
                }

                str += sign1 * jmp + sign2 * (2 + (1 - l) * 2 + 1);
            }
        }
    }

    str -= 2;

    // fill 
    for(int i=0; i<4 && count < pieces; i++){
        str+= 4;
        for( int j=0; j < 6 && count < pieces; j++){
            str += jmp + 1;  
            if(j%2){
                *str = '0';
                count++;
            }
            
        }
        str-= 6 * ( jmp + 1);
    }

    str -= 7*2;

    str-=2;

    for(int j=0; j<2 && count < pieces ; j++){
        if(j)
            str += -7*2 + 8*(jmp + 1);

        for(int i=0; i<4 && count < pieces ; i++){
            str+=4;
            *str = '0';
            count++;
        }
 
    }

}